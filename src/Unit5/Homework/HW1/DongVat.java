package Unit5.Homework.HW1;

public abstract class DongVat {

    private String ten, loai, mauSac, moiTruongSong, gioiTinh;

    public String getMoiTruongSong() {
        return moiTruongSong;
    }

    public void setMoiTruongSong(String moiTruongSong) {
        this.moiTruongSong = moiTruongSong;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getLoai() {
        return loai;
    }

    public void setLoai(String loai) {
        this.loai = loai;
    }

    public String getMauSac() {
        return mauSac;
    }

    public void setMauSac(String mauSac) {
        this.mauSac = mauSac;
    }

}
