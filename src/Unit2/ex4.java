public class ex4 {

    public static void main(String[] args) {
        int i = 1;
        for (; i <= 10;) {
            System.out.println(i + "- Day som tap the duc");
            i++;
        }
        int j = 1;
        while (j <= 10) {
            System.out.println(j + "- Day som tap the duc");
            j++;
        }
        int k = 1;
        do {
            System.out.println(k + "- Day som tap the duc");
            k++;
        } while (k <= 10);

    }
}
